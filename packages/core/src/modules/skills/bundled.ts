import type { Skill } from './types';

/**
 * 内置 research skill
 *
 * 多角度并行研究工作流：拆解问题 → 并行调查 → 汇编报告
 */
export const RESEARCH_SKILL: Skill = {
  name: 'research',
  description: '对一个主题进行深度研究，多角度并行调查并汇编报告',
  whenToUse: '用户要求研究、调研、深入了解某个话题，或使用 /research 命令',
  allowedTools: [
    'parallel_agent',
    'agent',
    'web_search',
    'web_fetch',
    'read_file',
    'write_file',
    'grep',
    'glob',
  ],
  effort: 'high',
  context: 'fork',
  paths: ['wiki/'],
  sourcePath: 'builtin',
  source: 'builtin',
  body: `# 深度研究工作流

你是一个研究协调者。当用户要求研究某个主题时，按照以下流程执行。

## 第一步：拆解问题

分析用户的研究主题，将其拆解为 **3-5 个独立的子问题**。每个子问题应该：
- 聚焦一个特定角度
- 可以独立调查（不依赖其他子问题的结果）
- 能通过网络搜索或文件分析得到答案

**拆解示例：**
- 用户问 "量子计算的现状" → 拆解为：技术进展、商业应用、主要玩家、技术挑战、未来趋势
- 用户问 "React vs Vue 怎么选" → 拆解为：性能对比、生态成熟度、学习曲线、企业采用率、社区活跃度

## 第二步：并行调查

使用 \`parallel_agent\` 工具同时派出多个研究 agent：

\`\`\`
parallel_agent({
  tasks: [
    { task: "研究 [子问题1]，使用 web_search 和 web_fetch 收集信息", label: "角度1" },
    { task: "研究 [子问题2]，使用 web_search 和 web_fetch 收集信息", label: "角度2" },
    { task: "研究 [子问题3]，使用 web_search 和 web_fetch 收集信息", label: "角度3" },
  ]
})
\`\`\`

每个 agent 的 task 描述要包含：
- 具体的研究方向
- 要求使用 web_search 搜索
- 要求使用 web_fetch 获取详细内容
- 要求记录信息来源 URL

## 第三步：汇编报告

收到所有并行结果后，综合成一份结构化报告：

\`\`\`markdown
# [研究主题]

## 摘要
[200字以内的核心发现]

## 详细发现

### [角度1]
[调查结果]
- 来源: [URL]

### [角度2]
[调查结果]
- 来源: [URL]

## 关键对比
| 维度 | 发现A | 发现B |
|------|-------|-------|

## 置信度评估
- [结论1]: 高置信度（多个独立来源一致）
- [结论2]: 中置信度（来源有限但可信）
- [结论3]: 低置信度（需要进一步验证）

## 待验证问题
- [需要进一步调查的问题]

## 信息来源
1. [URL1] - [简要描述]
2. [URL2] - [简要描述]
\`\`\`

## 第四步：保存结果

将报告写入 \`wiki/\` 目录，文件名格式：\`wiki/[主题关键词].md\`

## 注意事项

1. **并行 > 串行**：始终使用 parallel_agent，不要一个一个地调查
2. **来源追踪**：每个结论都要标注信息来源 URL
3. **置信度诚实**：不要夸大确定性，区分"确认的事实"和"推测"
4. **多语言**：如果用户用中文提问，报告用中文；用英文提问则用英文
5. **时效性**：注意信息的发布时间，优先使用最新的数据`,
};

