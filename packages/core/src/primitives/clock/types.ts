// ============================================================
// Clock - 时间抽象接口
// ============================================================
//
// 用于时间依赖的可测试设计。
// 默认使用系统时间，测试时可注入固定或可控时钟。
// ============================================================

/**
 * Clock 抽象接口
 * 用于获取当前时间戳（毫秒）
 */
export interface Clock {
  /** 获取当前时间戳（毫秒） */
  now(): number;
}

/**
 * 系统时钟（默认实现）
 * 使用 Date.now() 返回真实系统时间
 */
export const systemClock: Clock = {
  now: () => Date.now(),
};